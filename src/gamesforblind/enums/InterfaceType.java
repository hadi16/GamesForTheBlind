package gamesforblind.enums;

/**
 * Enumeration for the different types of keyboard interfaces that exist in the game.
 */
public enum InterfaceType {
    /**
     * Interface where the user selects a block & then a square within this selected block.
     */
    BLOCK_SELECTION_INTERFACE,

    /**
     * Interface where the user selects individual squares by pressing arrow keys.
     */
    ARROW_KEY_INTERFACE
}
